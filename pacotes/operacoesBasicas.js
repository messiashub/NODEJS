// MODULO COM FUNÇÕES

module.exports.soma = function soma(a, b) {                            // module.exports = EXPORTANDO MODULO
    return a + b;
}

module.exports.subtracao = function subtracao(a, b) {                 // module.exports = EXPORTANDO MODULO
    return a - b;
}

module.exports.multiplicacao = function multiplicacao(a, b) {          // module.exports = EXPORTANDO MODULO
    return a * b;
}

module.exports.divisao = function divisao(a, b) {                       // module.exports = EXPORTANDO MODULO
    return a / b;
}